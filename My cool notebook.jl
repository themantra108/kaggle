# ╔═╡ 8fa8d889-bc3f-4b7a-95e0-1666f9ff3543
# Load benchmark data from file
benchmarks = CSV.read("data//benchmarks.csv", DataFrame, header=["language", "benchmark", "time"])

# ╔═╡ c2c40668-d7a2-4d2b-ba78-82bbab7f5891
# Capitalize and decorate language names from datafile
dict = Dict(
    "c" => "C",
    "julia" => "Julia",
    "lua" => "LuaJIT",
    "fortran" => "Fortran",
    "java" => "Java",
    "javascript" => "JavaScript",
    "matlab" => "Matlab",
    "mathematica" => "Mathematica",
    "python" => "Python",
    "octave" => "Octave",
    "r" => "R",
    "rust" => "Rust",
    "go" => "Go"
)

# ╔═╡ 3c8d7acd-23ff-444e-9120-ac7adc9fa003
benchmarks.language .= [dict[lang] for lang in benchmarks.language]

# ╔═╡ 1dd0c846-616e-4e3e-9e86-24eee6114c49
# Normalize benchmark times by C times
ctime = filter(row -> row.language == "C", benchmarks)

# ╔═╡ 4c88faf1-0691-45f2-99f7-a7a93e4c85ee
merged = innerjoin(benchmarks, ctime, on=:benchmark, makeunique=true)

# ╔═╡ d0bd4f81-0cb2-4541-868f-ac644fb9593a
select!(merged, Not(:language_1))

# ╔═╡ b8e05b63-a62b-45c5-8a16-89c1aad842da
rename!(merged, :time_1 => :ctime)

# ╔═╡ e51e143d-11ea-49a0-b41b-bca67cd988e9
merged.normtime .= merged.time ./ merged.ctime

# ╔═╡ 8263d39b-9c5b-4e71-bad1-a59c487a6515
# Compute the geometric mean for each language
langs = String[]

# ╔═╡ d2a456c6-d4de-44bb-88ab-a1a78c6875cc
means = Float64[]

# ╔═╡ 02b4d57c-b1ec-40c2-bd02-92d2460ed4a7
priorities = Int[]

# ╔═╡ 4991b637-71c6-437e-bfa2-5020e4afcd19
for lang in unique(merged.language)
    	data = filter(row -> row.language == lang, merged)
    	gmean = geomean(data.normtime)
    	push!(langs, lang)
    	push!(means, gmean)
    	push!(priorities, lang == "C" ? 1 : lang == "Julia" ? 2 : 3)
	end

# ╔═╡ 91d2209d-0a2e-4b9e-ad12-ea6650b1f986
# Add the geometric means back into the benchmarks dataframe
	langmean = DataFrame(language=langs, geomean=means, priority=priorities)

# ╔═╡ a3c0bcfb-aaa7-48b5-8e0c-60a7b9da8bf9
langbenchmarks = innerjoin(merged, langmean, on=:language)

# ╔═╡ 7c117a89-e381-4d2d-b680-ced79c7ba3c9
# Put C first, Julia second, and sort the rest by geometric mean
	sort!(langbenchmarks, [:priority, :geomean])

# ╔═╡ 1ebd74a6-5634-48f3-8164-a93b791b5a76
sort!(langmean, [:priority, :geomean])

# ╔═╡ 7d2920f9-0928-4911-bdde-a498d311dad3
p = plot(
    langbenchmarks,
    x=:language,
    y=:normtime,
    color=:benchmark,
    Scale.y_log10,
    Guide.ylabel(nothing),
    Guide.xlabel(nothing),
    Theme(
        guide_title_position=:left,
        colorkey_swatch_shape=:circle,
        minor_label_font="Georgia",
        major_label_font="Georgia"
    ),
)

# ╔═╡ 0cdd3ec7-38f7-473d-85cc-ddd5cc4dfbb9
draw(SVG("data//benchmarks.svg", 9inch, 9inch / golden), p)

